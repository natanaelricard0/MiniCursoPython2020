# 3- Faça um programa que receba dois números inteiros e informe qual deles é o maior.


n1 = int(input('Informe o primeiro número: ')) #pede para o usuário o primeiro número
n2 = int(input('Informe o segundo número: ')) # pede para o usuário o segundo número

if n1 > n2: #se n1 for maior que n2 o programa irá mostrar na tela que o primeiro número é maior
    print('o primeiro número é o maior') 
else:
    print('o segundo valor é o maior') #se não atender a primeira condição (if) ele irá mostrar na tela que n2 é o maior