# 4- Faça um programa que receba dois números inteiros e informe qual deles é o menor.

n1 = int(input('Informe o primeiro número: '))
n2 = int(input('Informe o segundo número: '))

if n1 < n2: #se o primeiro número for menor que o segundo ele irá mostrar na tela que o primeiro é o menor número
    print('O primeiro número é o menor')

else: #se não ele mostra na tela que o segundo número é o menor
    print('O segundo número é o menor')
